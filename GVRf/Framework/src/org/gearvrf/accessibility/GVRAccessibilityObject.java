package org.gearvrf.accessibility;

import org.gearvrf.GVRAndroidResource;
import org.gearvrf.GVRContext;
import org.gearvrf.GVRMaterial.GVRShaderType.Texture;
import org.gearvrf.GVRMesh;
import org.gearvrf.GVRSceneObject;
import org.gearvrf.GVRTexture;

/**
 * {@code GVRAccessibilityObject} responsible to represent an Accessibility
 * scene object.
 * <p>
 */
public class GVRAccessibilityObject extends GVRSceneObject {

    private GVRAccessibilityTalkBack mTextSpeech;

    /**
     * Constructs a scene object with {@linkplain GVRMesh an arbitrarily complex
     * geometry} that uses the standard {@linkplain Texture 'texture shader'} to
     * display a {@linkplain GVRTexture texture.}
     * 
     * @param gvrContext current {@link GVRContext}
     * @param gvrMesh a {@link GVRMesh} - usually generated by one of the
     *            {@link GVRContext#loadMesh(GVRAndroidResource)} methods, or
     *            {@link GVRContext#createQuad(float, float)}
     * @param texture a {@link GVRTexture}
     */
    public GVRAccessibilityObject(GVRContext gvrContext, GVRMesh gvrMesh, GVRTexture texture) {
        super(gvrContext, gvrMesh, texture);
    }

    /**
     * Set the {@link GVRAccessibilityTExtSpeech}
     * 
     * @param mTextSpeech
     */
    public void setTextSpeech(GVRAccessibilityTalkBack mTextSpeech) {
        this.mTextSpeech = mTextSpeech;
    }

    /**
     * @return the object {@link GVRAccessibilityTalkBack}
     */
    public GVRAccessibilityTalkBack getTextSpeech() {
        return this.mTextSpeech;
    }
}
